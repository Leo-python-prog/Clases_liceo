# Igual a (==)
# Compara si dos valores son iguales.
# Ejemplo:

a = 5
b = 3
resultado = (a == b)
print(resultado)  # Resultado: False

# Distinto de (!=)
# Compara si dos valores son diferentes.
# Ejemplo:

a = 5
b = 3
resultado = (a != b)
print(resultado)  # Resultado: True

# Mayor que (>)
# Compara si el primer valor es mayor que el segundo.
# Ejemplo:

a = 5
b = 3
resultado = (a > b)
print(resultado)  # Resultado: True

# Menor que (<)
# Compara si el primer valor es menor que el segundo.
# Ejemplo:

a = 5
b = 3
resultado = (a < b)
print(resultado)  # Resultado: False

# Mayor o igual que (>=)
# Compara si el primer valor es mayor o igual que el segundo.
# Ejemplo:

a = 5
b = 5
resultado = (a >= b)
print(resultado)  # Resultado: True

# Menor o igual que (<=)
# Compara si el primer valor es menor o igual que el segundo.
# Ejemplo:

a = 5
b = 5
resultado = (a <= b)
print(resultado)  # Resultado: True