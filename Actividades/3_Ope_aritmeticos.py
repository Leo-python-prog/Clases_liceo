# Operadores básicos

# Suma (+)
# Ejemplo:

a = 5
b = 3

resultado = a + b

print(resultado)

# Resta (-)
# Ejemplo:

a = 5
b = 3

resultado = a - b

print(resultado)

# Multiplicación (*)
# Ejemplo:

a = 5
b = 3

resultado = a * b

print(resultado)

# División (/)
# Ejemplo:

a = 5
b = 3

resultado = a / b

print(resultado)

# División entera (//)
# Divide dos números y devuelve la parte entera del cociente
# Ejemplo:

a = 5
b = 3

resultado = a // b

print(resultado)

# Módulo (%)
# Devuelve el resto de la división de dos números.
# Ejemplo:

a = 5
b = 3

resultado = a % b

print(resultado)

# Exponente (**)
# Eleva un número a la potencia de otro.
# Ejemplo:

a = 5
b = 3

resultado = a ** b

print(resultado)